class AnimalModel {
    constructor(nome, tipo, sexo, porte, temperamento, status, descricao) {
        this.nome = nome;
        this.tipo = tipo;
        this.sexo = sexo;
        this.porte = porte;
        this.temperamento = temperamento;
        this.status = status;
        this.descricao = descricao;
    }
}

module.exports = AnimalModel;